import os
import random
from typing import Dict, Optional, Tuple

import numpy as np
import pandas as pd
import SimpleITK as sitk
import torch
from torch.utils.data import Dataset, DataLoader

from config import Config


class ProstateUnifiedDataset(Dataset):
    """
    Dataset loader for the revised RP dataset setup after 2026-06-23.

    New experimental logic:
      1) B1 / main baseline:
         - TCIA only
         - TBx-confirmed radiologist target lesion ROI supervision
         - positive and negative sampled target ROI voxels in the loss

      2) B2-B3 / biopsy-supervision ablations:
         - B2 uses TCIA SBx region labels only
         - B3 uses TCIA TBx-confirmed target ROIs + TCIA SBx regions
         - PROMIS held out for external validation
         - PUB radiologist annotations are retained only as legacy/reference
           supervision, not the main baseline

    Expected CSV files generated by the revised dataset setup script include, for example:
      - B1_TCIA_TBx_baseline_train.csv
      - B1_TCIA_TBx_baseline_internal_val.csv
      - B2_TCIA_SBx_only_train.csv
      - B3_TCIA_TBx_SBx_train.csv
      - N4_mixed_PROMIS_external_val.csv
      - legacy N1/N2/N3/N4 CSVs for comparison with older runs

    Returned supervision types:
      - lesion_mask: dense binary lesion mask from PUB radiologist annotation
      - target_mask: TCIA radiologist target lesion ROI, labelled by TBx-confirmed grade
      - zones_mask: anatomical systematic-biopsy zone ids, 0 for background
      - sys_labels: systematic-biopsy region labels, padded to 20 zones
      - gland_mask: dense prostate gland mask when available

    Important label convention:
      - sys_labels == INVALID_SYS_LABEL: invalid / unsampled / no supervision
      - sys_labels == 1: valid benign / negative biopsy region
      - sys_labels >= 2: any cancer, encoded as shifted ISUP labels
      - sys_labels >= CSPC_THRESHOLD: clinically significant PCa, if Config.CSPC_THRESHOLD is defined

    The Dataset does not force every case to have every label. Instead, each sample returns
    availability flags such as has_lesion, has_target, has_sys, and has_cls. The training loss
    should use these flags to decide which losses are active for each sample.
    """

    VALID_TASKS = {"mixed", "seg", "cls", "radiologist_only"}
    SOURCE_TO_ID = {"PUB": 0, "TCIA": 1, "PROMIS": 2}

    def __init__(
        self,
        csv_path: str,
        data_root: str,
        is_train: bool = True,
        task: str = "mixed",
        filter_by_task: bool = True,
    ):
        """
        Args:
            csv_path: Path to split CSV.
            data_root: Path to Unified_Dataset directory. Each patient folder should be under this root.
            is_train: Whether this dataset is used for training. Augmentation is only applied when True.
            task:
                - "mixed": keep cases with any supervision; use for N4 mixed supervision.
                - "seg": keep cases with radiologist lesion mask; use for segmentation-only training/validation.
                - "cls": keep cases with TBx or SBx supervision; use for classification training/validation.
                - "radiologist_only": keep PUB cases with lesion mask; use for N1 strong supervision baseline.
            filter_by_task: If True, filter rows according to the selected task.
        """
        if task not in self.VALID_TASKS:
            raise ValueError(f"Unknown task='{task}'. Valid options: {sorted(self.VALID_TASKS)}")

        if not os.path.exists(csv_path):
            raise FileNotFoundError(f"Split CSV not found: {csv_path}")

        self.df = pd.read_csv(csv_path)
        self.data_root = data_root
        self.is_train = is_train
        self.task = task
        self.filter_by_task = filter_by_task

        self.invalid_sys_label = int(getattr(Config, "INVALID_SYS_LABEL", -1))
        self.target_mask_filename = str(getattr(Config, "TCIA_TARGET_MASK_FILENAME", "target_mask.nii.gz"))

        # Project label convention:
        #   0 = background/unsampled, 1 = benign, 2 = ISUP1,
        #   3 = ISUP2, ..., 6 = ISUP5.
        # Therefore any cancer is label >= 2 and csPCa is label >= 3.
        self.cspc_threshold = int(getattr(Config, "CSPC_THRESHOLD", 2))

        self._validate_csv_columns()
        if self.filter_by_task:
            self.df = self._filter_dataframe_for_task(self.df, self.task)

        self.df = self.df.reset_index(drop=True)
        if len(self.df) == 0:
            raise ValueError(
                f"No samples left after loading {csv_path} with task='{task}'. "
                "Check the split CSV and supervision flags."
            )

    def _validate_csv_columns(self):
        required = {"patient_id", "source"}
        missing = required - set(self.df.columns)
        if missing:
            raise ValueError(f"CSV is missing required columns: {sorted(missing)}")

        # The revised setup script should generate these columns, but keep the loader robust.
        for col in ["has_target", "has_sys_12", "has_sys_20", "has_lesion", "has_gland"]:
            if col not in self.df.columns:
                self.df[col] = 0

    @classmethod
    def _filter_dataframe_for_task(cls, df: pd.DataFrame, task: str) -> pd.DataFrame:
        def as_int_series(col: str) -> pd.Series:
            return pd.to_numeric(df.get(col, 0), errors="coerce").fillna(0).astype(int)

        has_target = as_int_series("has_target") == 1
        has_sys = (as_int_series("has_sys_12") == 1) | (as_int_series("has_sys_20") == 1)
        has_lesion = as_int_series("has_lesion") == 1
        source = df["source"].astype(str)

        if task == "radiologist_only":
            return df[(source == "PUB") & has_lesion].copy()
        if task == "seg":
            return df[has_lesion].copy()
        if task == "cls":
            return df[has_target | has_sys].copy()
        # mixed: keep all supervised samples; this supports PUB + TCIA training and PROMIS external validation.
        return df[has_lesion | has_target | has_sys].copy()

    def __len__(self):
        return len(self.df)

    @staticmethod
    def _as_bool(value) -> bool:
        if pd.isna(value):
            return False
        try:
            return bool(int(value))
        except (ValueError, TypeError):
            return str(value).strip().lower() in {"true", "yes", "y"}

    @staticmethod
    def _load_nii(path: str) -> np.ndarray:
        return sitk.GetArrayFromImage(sitk.ReadImage(path))

    @staticmethod
    def _ensure_input_4d(input_tensor: np.ndarray) -> np.ndarray:
        """Return image as float32 channel-first tensor: (C, D, H, W)."""
        input_tensor = input_tensor.astype(np.float32)

        if input_tensor.ndim == 3:
            return np.expand_dims(input_tensor, axis=0)

        if input_tensor.ndim != 4:
            raise ValueError(f"input_tensor must be 3D or 4D, got shape {input_tensor.shape}")

        # Common case: already channel-first, e.g. (3, D, H, W)
        if input_tensor.shape[0] <= 8:
            return input_tensor

        # Robust fallback for channel-last arrays, e.g. (D, H, W, 3)
        if input_tensor.shape[-1] <= 8:
            return np.moveaxis(input_tensor, -1, 0).astype(np.float32)

        raise ValueError(
            f"Cannot infer channel dimension for input_tensor shape {input_tensor.shape}. "
            "Expected (C,D,H,W) or (D,H,W,C)."
        )

    @staticmethod
    def _ensure_mask_4d(mask: np.ndarray, expected_dhw: Tuple[int, int, int], name: str) -> np.ndarray:
        """Return mask as float32 tensor: (1, D, H, W)."""
        mask = mask.astype(np.float32)
        if mask.ndim == 4 and mask.shape[0] == 1:
            out = mask
        elif mask.ndim == 3:
            out = np.expand_dims(mask, axis=0)
        else:
            raise ValueError(f"{name} must be 3D or (1,D,H,W), got shape {mask.shape}")

        if tuple(out.shape[1:]) != tuple(expected_dhw):
            raise ValueError(
                f"{name} shape mismatch for loaded sample. Expected D,H,W={expected_dhw}, "
                f"but got {tuple(out.shape[1:])}. Check preprocessing / cropping consistency."
            )
        return out.astype(np.float32)

    def _apply_augmentations(self, input_tensor: np.ndarray, masks_dict: Dict[str, Optional[np.ndarray]]):
        """
        Simple 3D spatial augmentation.
        Only flips in axial X-Y plane are used. Z flip is avoided because it may invert base/apex anatomy.
        """
        if random.random() > 0.5:
            input_tensor = np.flip(input_tensor, axis=3).copy()  # Width / X
            for key, mask in masks_dict.items():
                if mask is not None:
                    masks_dict[key] = np.flip(mask, axis=3).copy()

        if random.random() > 0.5:
            input_tensor = np.flip(input_tensor, axis=2).copy()  # Height / Y
            for key, mask in masks_dict.items():
                if mask is not None:
                    masks_dict[key] = np.flip(mask, axis=2).copy()

        return input_tensor, masks_dict

    def _load_systematic_labels(self, p_dir: str, has_sys_12: bool, has_sys_20: bool) -> np.ndarray:
        """
        Load SBx labels and pad to 20 zones.
        TCIA usually has 12-zone labels; PROMIS usually has 20-zone labels.
        """
        sys_labels = np.full(20, self.invalid_sys_label, dtype=np.int64)

        if has_sys_12:
            path = os.path.join(p_dir, "systematic_labels_12.npy")
            s_labels = np.load(path).astype(np.int64)
            sys_labels[: min(12, len(s_labels))] = s_labels[:12]
        elif has_sys_20:
            path = os.path.join(p_dir, "systematic_labels_20.npy")
            s_labels = np.load(path).astype(np.int64)
            sys_labels[: min(20, len(s_labels))] = s_labels[:20]

        return sys_labels

    def _derive_sys_case_labels(self, sys_labels: np.ndarray, has_sys: bool):
        """
        Derive case-level classification labels from systematic biopsy regions.
        These labels are mainly used for cancer/no-cancer and csPCa validation.
        """
        if not has_sys:
            return self.invalid_sys_label, self.invalid_sys_label, self.invalid_sys_label

        valid = sys_labels[sys_labels != self.invalid_sys_label]
        if valid.size == 0:
            return self.invalid_sys_label, self.invalid_sys_label, self.invalid_sys_label

        max_grade = int(np.max(valid))
        cancer_label = int(max_grade >= 2)
        cspc_label = int(max_grade >= self.cspc_threshold)
        return max_grade, cancer_label, cspc_label

    def _derive_target_case_labels(self, target_mask: np.ndarray, has_target: bool):
        """
        Derive a case-level label from the TBx-confirmed radiologist target lesion mask.
        If the target ROI only encodes positive labels, all-zero target_mask is treated as benign
        when has_target=True. If you later add explicit target biopsy labels into the registry CSV,
        prefer using those CSV columns in the training script.
        """
        if not has_target:
            return self.invalid_sys_label, self.invalid_sys_label, self.invalid_sys_label

        max_grade = int(np.max(target_mask))
        cancer_label = int(max_grade >= 2)
        cspc_label = int(max_grade >= self.cspc_threshold)
        return max_grade, cancer_label, cspc_label

    def _combine_case_labels(self, target_grade: int, sys_grade: int):
        grades = [g for g in [target_grade, sys_grade] if g != self.invalid_sys_label]
        if len(grades) == 0:
            return self.invalid_sys_label, self.invalid_sys_label, self.invalid_sys_label, False

        max_grade = int(max(grades))
        cancer_label = int(max_grade >= 2)
        cspc_label = int(max_grade >= self.cspc_threshold)
        return max_grade, cancer_label, cspc_label, True

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        pid = str(row["patient_id"])
        source = str(row.get("source", "unknown"))
        p_dir = os.path.join(self.data_root, pid)

        input_path = os.path.join(p_dir, "input_tensor.npy")
        if not os.path.exists(input_path):
            raise FileNotFoundError(f"Missing input_tensor.npy for {pid}: {input_path}")

        input_tensor = self._ensure_input_4d(np.load(input_path))  # (C, D, H, W)
        D, H, W = input_tensor.shape[1:]
        expected_dhw = (D, H, W)

        # Empty defaults. Missing supervision is represented by zero masks plus availability flags.
        target_mask = np.zeros((1, D, H, W), dtype=np.float32)
        zones_mask = np.zeros((1, D, H, W), dtype=np.float32)
        lesion_mask = np.zeros((1, D, H, W), dtype=np.float32)
        gland_mask = np.zeros((1, D, H, W), dtype=np.float32)
        sys_labels = np.full(20, self.invalid_sys_label, dtype=np.int64)

        has_target = self._as_bool(row.get("has_target", 0))
        has_sys_12 = self._as_bool(row.get("has_sys_12", 0))
        has_sys_20 = self._as_bool(row.get("has_sys_20", 0))
        has_sys = has_sys_12 or has_sys_20
        has_lesion = self._as_bool(row.get("has_lesion", 0))
        has_gland = self._as_bool(row.get("has_gland", 0))

        masks_to_aug = {"target": None, "zones": None, "lesion": None, "gland": None}

        # A. TCIA TBx-confirmed radiologist target lesion supervision.
        if has_target:
            t_path = os.path.join(p_dir, self.target_mask_filename)
            if not os.path.exists(t_path):
                raise FileNotFoundError(
                    f"has_target=1 but {self.target_mask_filename} is missing for {pid}: {t_path}"
                )
            t_arr = self._load_nii(t_path)
            masks_to_aug["target"] = self._ensure_mask_4d(t_arr, expected_dhw, "target_mask")

        # B. TCIA/PROMIS systematic biopsy supervision.
        if has_sys:
            z_path = os.path.join(p_dir, "zones_mask.nii.gz")
            if not os.path.exists(z_path):
                raise FileNotFoundError(f"has_sys=1 but zones_mask.nii.gz is missing for {pid}: {z_path}")
            z_arr = self._load_nii(z_path)
            masks_to_aug["zones"] = self._ensure_mask_4d(z_arr, expected_dhw, "zones_mask")
            sys_labels = self._load_systematic_labels(p_dir, has_sys_12, has_sys_20)

        # C. PUB dense radiologist lesion mask for strong supervision segmentation.
        if has_lesion:
            l_path = os.path.join(p_dir, "lesion_mask.npy")
            if not os.path.exists(l_path):
                raise FileNotFoundError(f"has_lesion=1 but lesion_mask.npy is missing for {pid}: {l_path}")
            l_arr = np.load(l_path)
            l_arr = (l_arr > 0).astype(np.float32)
            masks_to_aug["lesion"] = self._ensure_mask_4d(l_arr, expected_dhw, "lesion_mask")

        # D. Dense gland mask. PUB may store npy; TCIA/PROMIS may store nii.gz.
        if has_gland:
            g_path_nii = os.path.join(p_dir, "gland_mask.nii.gz")
            g_path_npy = os.path.join(p_dir, "gland_mask.npy")
            if os.path.exists(g_path_nii):
                g_arr = self._load_nii(g_path_nii)
            elif os.path.exists(g_path_npy):
                g_arr = np.load(g_path_npy)
            else:
                raise FileNotFoundError(
                    f"has_gland=1 but neither gland_mask.nii.gz nor gland_mask.npy exists for {pid}"
                )
            g_arr = (g_arr > 0).astype(np.float32)
            masks_to_aug["gland"] = self._ensure_mask_4d(g_arr, expected_dhw, "gland_mask")

        # Apply spatial augmentation only to images and spatial masks.
        if self.is_train and getattr(Config, "USE_AUGMENTATION", False):
            input_tensor, masks_to_aug = self._apply_augmentations(input_tensor, masks_to_aug)

        if masks_to_aug["target"] is not None:
            target_mask = masks_to_aug["target"]
        if masks_to_aug["zones"] is not None:
            zones_mask = masks_to_aug["zones"]
        if masks_to_aug["lesion"] is not None:
            lesion_mask = masks_to_aug["lesion"]
        if masks_to_aug["gland"] is not None:
            gland_mask = masks_to_aug["gland"]

        # Optional conflict handling: remove TBx-confirmed target ROI voxels from SBx zone mask.
        # This prevents strong/weak labels from competing at exactly the same voxels.
        if getattr(Config, "MASK_TARGET_IN_SYS", False) and has_target and has_sys:
            zones_mask[target_mask > 0] = 0

        # Case-level labels for classification tasks.
        sys_grade, sys_cancer, sys_cspc = self._derive_sys_case_labels(sys_labels, has_sys)
        target_grade, target_cancer, target_cspc = self._derive_target_case_labels(target_mask, has_target)
        cls_grade, cls_cancer, cls_cspc, has_cls = self._combine_case_labels(target_grade, sys_grade)

        # If later your split CSV contains explicit case-level labels, they override derived labels.
        # This makes the loader compatible with a future supervisor-provided split table.
        if "case_grade" in row and not pd.isna(row["case_grade"]):
            cls_grade = int(row["case_grade"])
            cls_cancer = int(cls_grade > 0)
            cls_cspc = int(cls_grade >= self.cspc_threshold)
            has_cls = True
        if "cancer_label" in row and not pd.isna(row["cancer_label"]):
            cls_cancer = int(row["cancer_label"])
            has_cls = True
        if "cspc_label" in row and not pd.isna(row["cspc_label"]):
            cls_cspc = int(row["cspc_label"])
            has_cls = True

        source_id = self.SOURCE_TO_ID.get(source, -1)
        is_external = bool(str(row.get("split", "")).lower() in {"external", "external_val", "external_validation"})
        if source == "PROMIS":
            # In the revised setup, PROMIS is normally the external validation domain.
            is_external = True

        return {
            # Metadata. PyTorch default collate will keep strings as lists.
            "pid": pid,
            "source": source,
            "source_id": torch.tensor(source_id, dtype=torch.long),
            "is_external": torch.tensor(float(is_external), dtype=torch.float32),

            # Input.
            "input": torch.from_numpy(input_tensor.astype(np.float32)),

            # Spatial labels.
            "target_mask": torch.from_numpy(target_mask.astype(np.float32)),
            "zones_mask": torch.from_numpy(zones_mask.astype(np.float32)),
            "lesion_mask": torch.from_numpy(lesion_mask.astype(np.float32)),
            "gland_mask": torch.from_numpy(gland_mask.astype(np.float32)),

            # SBx labels.
            "sys_labels": torch.from_numpy(sys_labels.astype(np.int64)),

            # Availability flags for loss masking.
            "has_target": torch.tensor(float(has_target), dtype=torch.float32),
            "has_sys": torch.tensor(float(has_sys), dtype=torch.float32),
            "has_sys_12": torch.tensor(float(has_sys_12), dtype=torch.float32),
            "has_sys_20": torch.tensor(float(has_sys_20), dtype=torch.float32),
            "has_lesion": torch.tensor(float(has_lesion), dtype=torch.float32),
            "has_gland": torch.tensor(float(has_gland), dtype=torch.float32),
            "has_cls": torch.tensor(float(has_cls), dtype=torch.float32),

            # Derived case-level labels for classification.
            "target_grade": torch.tensor(target_grade, dtype=torch.long),
            "target_cancer_label": torch.tensor(target_cancer, dtype=torch.long),
            "target_cspc_label": torch.tensor(target_cspc, dtype=torch.long),
            "sys_grade": torch.tensor(sys_grade, dtype=torch.long),
            "sys_cancer_label": torch.tensor(sys_cancer, dtype=torch.long),
            "sys_cspc_label": torch.tensor(sys_cspc, dtype=torch.long),
            "cls_grade": torch.tensor(cls_grade, dtype=torch.long),
            "cls_cancer_label": torch.tensor(cls_cancer, dtype=torch.long),
            "cls_cspc_label": torch.tensor(cls_cspc, dtype=torch.long),
        }


def build_dataloader(
    csv_path: str,
    data_root: str,
    batch_size: int,
    is_train: bool,
    task: str = "mixed",
    num_workers: Optional[int] = None,
    shuffle: Optional[bool] = None,
) -> DataLoader:
    """Small helper for train/validation scripts."""
    dataset = ProstateUnifiedDataset(
        csv_path=csv_path,
        data_root=data_root,
        is_train=is_train,
        task=task,
        filter_by_task=True,
    )
    if num_workers is None:
        num_workers = int(getattr(Config, "NUM_WORKERS", 4))
    if shuffle is None:
        shuffle = bool(is_train)

    return DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=shuffle,
        num_workers=num_workers,
        pin_memory=bool(getattr(Config, "PIN_MEMORY", True)),
    )


